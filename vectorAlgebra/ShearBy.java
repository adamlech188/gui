package cs3744.vectorAlgebra;


/**
 * Enumerates the options for shearing.
 * @author Peter J. Radics
 * @version 1.0
 *
 */
public enum ShearBy {
    /**
     * Shear x by y
     */
    X_BY_Y,
    /**
     * Shear x by z
     */
    X_BY_Z,
    /**
     * Shear y by x
     */
    Y_BY_X,
    /**
     * Shear y by z
     */
    Y_BY_Z,
    /**
     * Shear z by x
     */
    Z_BY_X,
    /**
     * Shear z by y
     */
    Z_BY_Y;
}
