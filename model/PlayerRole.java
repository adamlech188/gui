package cs3744.model;


/**
 * The <CODE>PlayerRole</CODE> enum enumerates the different roles of a player.
 * 
 * @author Peter J. Radics
 * @version 1.0
 * 
 */
public enum PlayerRole {

    /**
     * None
     */
    NONE,
    /**
     * Observer
     */
    OBSERVER,
    /**
     * The first player
     */
    PLAYER_ONE,
    /**
     * The second player
     */
    PLAYER_TWO,
    /**
     * The third player
     */
    PLAYER_THREE,
    /**
     * The fourth player
     */
    PLAYER_FOUR
}
