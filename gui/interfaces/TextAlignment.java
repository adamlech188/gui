package cs3744.gui.interfaces;


/**
 * The <CODE>TextAlignment</CODE> enum enumerates over the possible text
 * alignments.
 * 
 * @author Peter J. Radics
 * @version 1.0
 * 
 */
public enum TextAlignment {
    /**
     * Text that is aligned to the left.
     */
    LEFT,
    /**
     * Text that is centered.
     */
    CENTER,
    /**
     * Text that is aligned to the right.
     */
    RIGHT;
}
