package cs3744.graphics.common;


/**
 * The <CODE>ProjectionMode</CODE> enum enumerates over the different possible
 * projection modes.
 * 
 * @author Peter J. Radics
 * @version 1.0
 * 
 */
public enum ProjectionMode {
    /**
     * 2D orthographic projection.
     */
    ORTHOGRAPHIC_2D,
    /**
     * 3D orthographic projection.
     */
    ORTHOGRAPHIC_3D,
    /**
     * 3D perspective projection.
     */
    PERSPECTIVE;
}
