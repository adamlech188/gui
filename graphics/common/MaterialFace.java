package cs3744.graphics.common;


/**
 * The <CODE>MaterialFace</CODE> Enum enumerates the faces a material can be
 * applied to.
 * 
 * @author Peter J. Radics
 * @version 1.0
 */
public enum MaterialFace {
    /**
     * The front face.
     */
    FRONT,
    /**
     * The back face.
     */
    BACK,
    /**
     * Both the front and back face.
     */
    FRONT_AND_BACK;
}
